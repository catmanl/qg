int main() {
  int w = WIDTH*TILE_SIZE;
  int h = HEIGHT*TILE_SIZE;
  QGameTileset *ts;
  QGameTilemap *tm;
  
  qg_init_window(w, h, "[qgame] tilemap");
  qg_set_fps(60);

  ts = qgame_tileset_new("tileset.png", TILE_SIZE, TILE_SIZE);
  tm = qgame_tilemap_new();
  tm->ts = ts;
  tm->x = 0;
  tm->y = 0;
  qgame_tilemap_init_size(tm, WIDTH, HEIGHT);
  qgame_tilemap_set_data(tm, *map, HEIGHT*WIDTH);

  while (qg_window_is_open()) {
    qg_begin_drawing();
    qg_clear(QG_BLACK);
    qgame_tilemap_draw_ex(tm, 0, 0, w, h);
    qgame_tilemap_draw_grid(tm, QG_WHITE);
    qg_end_drawing();
  }

  qgame_tileset_free(ts);
  qgame_tilemap_free(tm);
  qg_close_window();
  return 0;
}
